public class PointsBean {

    private String points;
    private String points_grade;
    private String points_title;
    private String evaluation_time;

    public String getPoints() {
        return points;
    }

    public void setPoints(String points) {
        this.points = points;
    }

    public String getPoints_grade() {
        return points_grade;
    }

    public void setPoints_grade(String points_grade) {
        this.points_grade = points_grade;
    }

    public String getPoints_title() {
        return points_title;
    }

    public void setPoints_title(String points_title) {
        this.points_title = points_title;
    }

    public String getEvaluation_time() {
        return evaluation_time;
    }

    public void setEvaluation_time(String evaluation_time) {
        this.evaluation_time = evaluation_time;
    }
}
